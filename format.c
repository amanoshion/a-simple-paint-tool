void ini_palete(char *data, RGBQUAD *palete, int limit) {
    int j = 0;
    for (int i = 0; i < limit; i++) {
            palete[i].blue = data[j++];
            palete[i].green = data[j++];
            palete[i].red = data[j++];
            palete[i].reserved = 0;
            j++;
    }
    return;
}
void make_palete(Deep_Type type) {
    switch(type) {
        case bit_1:
            RGBQUAD palette[2];
            ini_palete(bit_1_palette_data, palette, 2);
            break;

        case bit_4:
            RGBQUAD palette[16];
            ini_palete(bit_4_palette_data, palette, 4);
            break;

        case bit_8:
            RGBQUAD palette[256];
            ini_palete(bit_8_palette_data, palette, 4);
            break;
        
        default:
            break;
    }
    return;
}


void calc_size(FILE *fp) {
    fseek(fp, 0, SEEK_END);
    int size = ftell(fp);
    char size_buffer[2] = {0};
    snprintf(size_buffer, 2, "%d", size);
    rewind(fp);
    fseek(fp, sizeof(uint16_t), SEEK_SET);
    fwrite(size_buffer, sizeof(uint32_t), 1, fp);
}

